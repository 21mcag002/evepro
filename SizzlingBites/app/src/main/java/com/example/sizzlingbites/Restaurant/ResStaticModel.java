package com.example.sizzlingbites.Restaurant;

public class ResStaticModel {
    private int img, pos;

    public ResStaticModel(int img, int pos) {
        this.img = img;
        this.pos = pos;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }
}
